public class Data {

    private String mTimeStamp;
    private String mOpen;
    private String mClose;
    private String mHigh;
    private String mLow;
    private String mVol;
    private String mOi;

    public String getTimeStamp() {
        return mTimeStamp;
    }

    public void setTimeStamp(String mTimeStamp) {
        this.mTimeStamp = mTimeStamp;
    }

    public String getOpen() {
        return mOpen;
    }

    public void setOpen(String mOpen) {
        this.mOpen = mOpen;
    }

    public String getClose() {
        return mClose;
    }

    public void setClose(String mClose) {
        this.mClose = mClose;
    }

    public String getHigh() {
        return mHigh;
    }

    public void setHigh(String mHigh) {
        this.mHigh = mHigh;
    }

    public String getLow() {
        return mLow;
    }

    public void setLow(String mLow) {
        this.mLow = mLow;
    }

    public String getVol() {
        return mVol;
    }

    public void setVol(String mVol) {
        this.mVol = mVol;
    }

    public String getOi() {
        return mOi;
    }

    public void setOi(String mOi) {
        this.mOi = mOi;
    }
}
